for (const course of coursesArray) {
    let div = document.createElement('div');
    let title = document.createElement('h3');
    let double = document.createElement('div');
    let month = document.createElement('div');
    let hours = document.createElement('div');
    let ul = document.createElement('ul');

    for (const module of course.modules) {
        let modules = document.createElement('li');
        modules.innerText = `${course.modules}`;
        ul.appendChild(modules);
    }

    title.innerText = `${course.title}`;
    month.innerText = `${course.monthDuration}`;
    hours.innerText = `${course.hourDuration}`;

    double.style.display = 'flex';
    double.style.flexDirection = 'row';
    month.style.marginRight = '50px';


    double.appendChild(month);
    double.appendChild(hours);


    div.appendChild(title);
    div.appendChild(double);
    div.appendChild(ul);
    document.body.appendChild(div);
}